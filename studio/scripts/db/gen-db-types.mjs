import { execFileSync } from 'node:child_process'
import fs from 'node:fs'
import path from 'node:path'

import { computeMigrationsHash } from './schema-hash.mjs'

const repoRoot = path.resolve(process.cwd(), '..')
const migrationsDir = path.join(repoRoot, 'supabase', 'migrations')
const outFile = path.join(process.cwd(), 'src', 'lib', 'supabase', 'database.types.ts')

const { hash: schemaHash, files } = computeMigrationsHash({ migrationsDir })

function runSupabaseGen() {
  try {
    return execFileSync('supabase', ['gen', 'types', 'typescript', '--local'], {
      cwd: repoRoot,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'pipe'],
    })
  } catch (error) {
    const stderr = error?.stderr ? String(error.stderr) : ''
    const hint =
      'Failed to run `supabase gen types`. Make sure Supabase is running (e.g. `supabase start` or `supabase db reset`).'

    console.error(stderr || hint)
    console.error(hint)
    process.exit(1)
  }
}

const generated = runSupabaseGen().trimEnd() + '\n'

const header =
  `/**\n` +
  ` * AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.\n` +
  ` *\n` +
  ` * Generated by: npm run db:types:generate\n` +
  ` * Source: supabase/migrations/*.sql (hash: ${schemaHash})\n` +
  ` * Files: ${files.join(', ')}\n` +
  ` */\n\n`

fs.mkdirSync(path.dirname(outFile), { recursive: true })
fs.writeFileSync(outFile, header + generated, 'utf8')

try {
  execFileSync('npx', ['prettier', '--write', outFile], {
    cwd: process.cwd(),
    stdio: 'ignore',
  })
} catch (error) {
  const message = error instanceof Error ? error.message : String(error)
  console.warn(`[db:types] Prettier failed (continuing): ${message}`)
}

console.log(`[db:types] Wrote ${path.relative(process.cwd(), outFile)} (schema-hash ${schemaHash.slice(0, 12)}…)`)
